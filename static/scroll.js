<script>
  var chatbox = document.getElementById("chatbox");
  chatbox.scrollTop = chatbox.scrollHeight;
</script>



// Ce script définit la propriété scrollTop d'un élément HTML avec l'identifiant "chatbox" à sa scrollHeight,
// permettant ainsi de faire défiler automatiquement l'élément jusqu'en bas.
// Cela peut être utile dans une application de chat ou dans toute autre situation où vous souhaitez faire
// défiler automatiquement vers le bas un élément conteneur qui affiche une conversation ou d'autres contenus.